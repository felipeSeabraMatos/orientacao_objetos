package models;

public class CarroCorrida {
    String ferrari;
    String mercedes;
    String redBull;

    public String getFerrari() {
        return ferrari;
    }

    public void setFerrari(String ferrari) {
        this.ferrari = ferrari;
    }

    public String getMercedes() {
        return mercedes;
    }

    public void setMercedes(String mercedes) {
        this.mercedes = mercedes;
    }

    public String getRedBull() {
        return redBull;
    }

    public void setRedBull(String redBull) {
        this.redBull = redBull;
    }

}

